package exercicio.model;

public class Arquivo {

	private String md5;
	
	private String chave;
	
	public void setMd5(String md5) {
		this.md5 = md5;
	}
	
	public String getMd5() {
		return md5;
	}
	
	public void setChave(String chave) {
		this.chave = chave;
	}
	
	public String getChave() {
		return chave;
	}
}
